// Создать html-страницу с футбольным полем, которое занимает всю ширину и высоту экрана,
// и мячом размером 100 на 100 пикселей. 
// Сделать так, чтобы при клике мышкой по полю, мяч плавно перемещался на место клика. 
// Учтите: необходимо, чтобы центр мяча останавливался именно там, где был совершен клик мышкой. 
// Также предусмотрите, чтобы мяч не выходил за границы поля.
// ========================================================================================================










// Получаем элемент футбольного поля
const field = document.getElementById('field');

// Получаем элемент мяча
const ball = document.getElementById('ball');

// Добавляем обработчик события на клик по полю
field.addEventListener('click', (event) => {

    // Получаем координаты и размеры поля относительно окна браузера
    const fieldRect = field.getBoundingClientRect();

    // Получаем размер мяча (ширина и высота одинаковые)
    const ballSize = ball.offsetWidth;

    // Координаты клика относительно поля:
    // event.clientX и event.clientY — координаты клика относительно окна
    // fieldRect.left и fieldRect.top — координаты верхнего левого угла поля
    // Вычитаем половину размера мяча, чтобы центр мяча совпал с кликом
    let x = event.clientX - fieldRect.left - ballSize / 2;
    let y = event.clientY - fieldRect.top - ballSize / 2;

    // Ограничиваем координаты, чтобы мяч не вышел за границы поля:
    // Math.max(0, value) — гарантирует, что значение не будет меньше 0 (левая и верхняя границы)
    // Math.min(maxValue, value) — гарантирует, что значение не будет больше максимального (правая и нижняя границы)
    x = Math.max(0, Math.min(fieldRect.width - ballSize, x));
    y = Math.max(0, Math.min(fieldRect.height - ballSize, y));

    // Устанавливаем позицию мяча через стили CSS
    // Значения задаются в пикселях
    ball.style.left = `${x}px`;
    ball.style.top = `${y}px`;
});


